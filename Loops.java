public class Loops {
    public static void main(String[] args) {
        // for loop
        for (int i=0;i<10;++i)
        {
            System.out.println(i);
        }

        int j=10;
        while(j>=1)
        {
            System.out.println(j);
            j-=1;
        }

        int k=20;
        do{
            System.out.println(k);
            k-=1;
        }while(k>=21);


    }
}
