public class Constants {
    public static void main(String[] args) {
        //constants
        int age=13;
        age=31;
        System.out.println(age);

        float f=3.12F;
        f=31.2F;
        System.out.println(f);

        //for const 
        //1. write or start with final and variable name should be in Capital
        
        final float PI=178.3F;
        // PI=899.77F;
        System.out.println(PI);


    }
}
